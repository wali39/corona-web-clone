$(".over1").on({
  mouseenter: function () {
    $(".image1").css({
      transform: "scale(1.2)",
      backgroundColor: "gray",
    });
  },
  mouseleave: function () {
    $(".image1").css({
      transform: "scale(1)",
      backgroundColor: "transparent",
    });
  },
});
